package LearnStarPatterns;

public class Star1 {
	public static void main(String[] args) {
		int lines = 2;
		int numberOfStars = 15;
		for (int i = 1; i <= lines; i++) {
			for (int j = 1; j <= numberOfStars; j++) {
				System.out.print("*");
			}
			System.out.println();
		}
	}
}

